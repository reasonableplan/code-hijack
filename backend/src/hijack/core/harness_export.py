# shared-lessons-candidates.md
# ---------------------------------------------------------------------------

def _render_lesson_candidates(
    session: SessionResult, summary: ExportSummary
) -> str:
    domain_rules = _filter_rules(session, scope="domain_specific")
    has_anti_patterns = any(cat.anti_patterns for cat in session.categories)
    if not domain_rules and not has_anti_patterns:
        return ""

    lines = [
        "# LESSON 후보 — code-hijack 추출",
        "",
        f"> Generated by code-hijack from `{session.target}`",
        "",
        "> 이 문서는 LESSON 후보 모음. 사용자가 검토 후 `backend/docs/shared-lessons.md`",
        "> 의 LESSON-NNN 으로 등록 — 자동 등록 X.",
        "",
    ]

    # Anti-patterns
    if has_anti_patterns:
        lines += ["## Anti-Patterns (자동 추출)", ""]
        for cat in session.categories:
            for ap in cat.anti_patterns:
                pattern = ap.get("pattern", "")
                if not pattern:
                    continue
                lines += [
                    f"### [{cat.category}] {pattern}",
                    "",
                    f"**Why**: {ap.get('reason', '')}",
                    "",
                    f"**Alternative**: {ap.get('alternative', '')}",
                    "",
                ]

    # Domain-specific rules — 새 도메인에선 재평가 필요
    if domain_rules:
        lines += [
            "## Domain-Specific Rules (재평가 필요)",
            "",
            "> 아래 규칙들은 추출된 프로젝트의 도메인 결정을 반영. 다른 도메인에",
            "> 적용 시 값/범위/이름 변경이 필요할 수 있음. 사용자가 적합성 판단 후 LESSON 으로 등록.",
            "",
        ]
        for rule in domain_rules:
            lines += [
                f"### {rule.rule}",
                "",
                f"**Layer**: `{rule.layer}` | **Priority**: `{rule.priority}`",
                "",
                f"**Why**: {rule.reason}",
                "",
            ]
            if rule.ref_files:
                lines.append(
                    f"**Reference**: {', '.join(f'`{f}`' for f in rule.ref_files)}"
                )
                lines.append("")

    return "\n".join(lines)


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _filter_rules(session: SessionResult, scope: str) -> list[AnalysisRule]:
    return [
        rule
        for cat in session.categories
        for rule in cat.rules
        if (rule.scope or "cross_project") == scope
    ]


def _topic_from_rule(rule_text: str) -> str:
    """결정 표의 '주제' 컬럼 — rule 의 첫 의미 단어 4-6 단어로 압축."""
    words = rule_text.replace("`", "").split()
    if len(words) <= 4:
        return " ".join(words)
    # 첫 4 단어 + ... (간결)
    return " ".join(words[:4]) + "…"


def _truncate(text: str, max_len: int) -> str:
    text = text.replace("\n", " ").strip()
    if len(text) <= max_len:
        return text
    return text[: max_len - 1] + "…"


def _is_db_category(session: SessionResult, category: str) -> bool:
    """해당 category 의 cross_project rule 이 db layer 에 하나라도 있는지."""
    for cat in session.categories:
        if cat.category != category:
            continue
        for rule in cat.rules:
            if rule.layer == "db" and (rule.scope or "cross_project") == "cross_project":
                return True
    return False


def _group_rules_for_index(
    session: SessionResult,
) -> dict[tuple[str, str], int]:
    """가이드 색인용 (layer_dir, category) → count 카운터."""
    out: dict[tuple[str, str], int] = {}
    for cat in session.categories:
        if cat.category not in _CATEGORY_TO_FILENAME:
            continue
        for rule in cat.rules:
            if (rule.scope or "cross_project") != "cross_project":
                continue
            layer_dir = _LAYER_TO_DIR.get(rule.layer)
            if layer_dir is None:
                continue
            key = (layer_dir, cat.category)
            out[key] = out.get(key, 0) + 1
    return out
