def _write_guidelines(session: SessionResult, output_dir: Path) -> list[Path]:
    """layer × category 조합마다 guidelines/<layer-dir>/<file>.md 생성."""
    written: list[Path] = []
    guidelines_root = output_dir / "guidelines"

    # 그룹: (layer_dir, filename) → rules
    grouped: dict[tuple[str, str], list[AnalysisRule]] = {}
    for cat in session.categories:
        filename = _CATEGORY_TO_FILENAME.get(cat.category)
        if filename is None:
            continue  # 미매핑 카테고리 (e.g. dependencies) 는 건너뜀
        for rule in cat.rules:
            if (rule.scope or "cross_project") != "cross_project":
                continue
            layer_dir = _LAYER_TO_DIR.get(rule.layer)
            if layer_dir is None:
                continue  # shared 는 conventions 로 흡수
            target_filename = (
                f"db-{filename}" if rule.layer == "db" else filename
            )
            grouped.setdefault((layer_dir, target_filename), []).append(rule)

    for (layer_dir, filename), rules in sorted(grouped.items()):
        target = guidelines_root / layer_dir / filename
        target.parent.mkdir(parents=True, exist_ok=True)
        # category 는 첫 rule 의 source category 로 추론 — grouping 키와 무관
        category = next(
            (
                cat.category
                for cat in session.categories
                if any(r in cat.rules for r in rules)
            ),
            "",
        )
        body = _render_guideline(layer_dir, category, rules, session=session)
        target.write_text(body, encoding="utf-8")
        written.append(target)

    return written


def _render_guideline(
    layer_dir: str,
    category: str,
    rules: list[AnalysisRule],
    *,
    session: SessionResult | None = None,
) -> str:
    title_layer = layer_dir.title()
    title_category = category.replace("_", " ").title() if category else ""
    title = f"{title_layer} — {title_category}".rstrip(" —")
    lines = [
        f"# {title}",
        "",
        f"> code-hijack 추출. cross_project scope 만 포함 ({len(rules)} rules).",
        "",
    ]
    if session is not None:
        # 카테고리의 design_intent 가 있으면 첨부
        for cat in session.categories:
            if cat.category == category and cat.design_intent:
                lines += ["## Design Intent", "", cat.design_intent, ""]
                break

    lines += ["## Rules", ""]
    for rule in rules:
        lines += _render_full_rule(rule)

    return "\n".join(lines)


def _render_full_rule(rule: AnalysisRule) -> list[str]:
    out = [
        f"### {rule.rule}",
        "",
        f"**Priority**: `{rule.priority}` | **Confidence**: `{rule.confidence}`"
        f" | **Layer**: `{rule.layer}`",
        "",
        f"**Why**: {rule.reason}",
        "",
    ]
    if rule.ref_files:
        out += [f"**Reference**: {', '.join(f'`{f}`' for f in rule.ref_files)}", ""]
    if rule.good_example:
        out += ["**✅ Good**:", "```", rule.good_example, "```", ""]
    if rule.bad_example:
        out += ["**❌ Bad**:", "```", rule.bad_example, "```", ""]
    return out


